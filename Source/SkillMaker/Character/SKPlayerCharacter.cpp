void ASKPlayerCharacter::UseSkill(const FSkillData& SkillData)
{
	if(!SkillData.SkillMontage || !GetMesh() || !GetMesh()->GetAnimInstance()) return;

	GetMesh()->GetAnimInstance()->Montage_Play(SkillData.SkillMontage, 1.0f);
}
